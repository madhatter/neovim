-- Use tabs instead of spaces for indentation in C files
vim.opt_local.expandtab = false

-- Set the width of a hard tabstop to 4 spaces
vim.opt_local.tabstop = 4

-- Set the width of an indentation level to 4 spaces
vim.opt_local.shiftwidth = 4

-- Disable the use of spaces for autoindentation
vim.opt_local.softtabstop = 0

-- Enable smart indentation for C files
vim.opt_local.smartindent = true

